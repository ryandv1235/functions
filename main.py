def reverseString(text):
	return text[::-1]

def splitString(text):
	return text.split()

def reverseList():
	lijst = ["hey", "lol", "hallo"]
	return lijst[::-1]

def multiplySelf(number):
	return number * number


def getHalf(number):
	return number / 2


print(reverseString("hey"))
print(splitString("Hey ik ben Ryan"))
print(multiplySelf(5))
print(getHalf(12))
